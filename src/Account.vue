<template>
  <div>
    <el-input
      style="width:280px;"
      placeholder="请输入筛选条件"
      v-model="filter"
      size="medium"
      clearable
    ></el-input>
    <hr />
    <PagedTable
      :tableData="tableData"
      :tableHeader="tableHeader"
      :filter="filter"
      :loading="loading"
    >
      <el-table-column label="AP点数" width="150px" v-if="account.admin">
        <template slot-scope="scope">
          {{ scope.row.point }}
        </template>
      </el-table-column>
      <el-table-column label="状态" width="80px">
        <template slot-scope="scope">
          <el-tag
            type="success"
            size="medium"
            disable-transitions
            v-if="!scope.row.disable"
            >有 效</el-tag
          >
          <el-tag
            type="danger"
            size="medium"
            disable-transitions
            v-if="scope.row.disable"
            >无 效</el-tag
          >
        </template>
      </el-table-column>
      <el-table-column label="操作" width="160px" v-if="account.admin">
        <template slot-scope="scope">
          <el-popover
            placement="bottom"
            width="200"
            :ref="`popover-${scope.$index}`"
          >
            <p>
              <i class="el-icon-warning"></i>
              账号删除后系统将中止正在进行的推流任务, 是否继续?
            </p>
            <div style="text-align: right; margin-top:8px;">
              <el-button
                type="primary"
                size="mini"
                @click="removeAccount(scope.row.accountId, scope.row.accountSite, scope.$index)"
                >继续</el-button
              >
            </div>
            <el-button slot="reference" size="mini" type="danger"
              >删除</el-button
            >
          </el-popover>
          <el-button
            size="mini"
            type="primary"
            @click="billList(scope.row.accountId, scope.row.accountSite, scope.$index)"
            >账单</el-button
          >
        </template>
      </el-table-column>
    </PagedTable>
    <el-dialog
      :title="`${billAccountId}的账单`"
      :visible.sync="billListVisible"
    >
      <el-button
        size="mini"
        type="primary"
        @click="apPointChange(billAccountId, billAccountSite)"
        >AP点数变更</el-button
      >
      <PagedTable
        :tableData="billTableData"
        :tableHeader="billTableHeader"
        :loading="loading"
      ></PagedTable>
    </el-dialog>
  </div>
</template>

<style>
.el-icon-warning {
  color: #e6a23c;
}

.el-input.is-disabled .el-input__inner,
.el-textarea.is-disabled .el-textarea__inner {
  background-color: #fff;
  border-color: #dcdfe6;
  color: #606266;
  cursor: default;
}
</style>


<script>
import PagedTable from "./PagedTable.vue";
export default {
  data() {
    return {
      account: JSON.parse(sessionStorage.getItem("account")),
      tableData: [],
      tableHeader: [
        { prop: "accountId", label: "账号标识" },
        { prop: "nickname", label: "推流账号" },
        { prop: "roomId", label: "直播间ID" },
        { prop: "accountSite", label: "推流平台" },
        { prop: "parentAccountName", label: "父账号" },
        { prop: "description", label: "描述" }
      ],
      loading: false,
      billTableData: [],
      billTableHeader: [
        { prop: "recordDate", label: "记录时间", width: "250px" },
        { prop: "pointChange", label: "AP点数变动", width: "150px" },
        { prop: "remark", label: "描述" }
      ],
      billListVisible: false,
      billAccountId: "",
      billAccountSite:"",
      detailVisible: false,
      editVisible: false,
      addVisible: false,
      agree: false,
      accountInfo: {},
      filter: ""
    };
  },
  methods: {
    accountList() {
      this.loading = true;
      this.$http.get("/api/account/accountList.json").then(
        function(response) {
          // 这里是处理正确的回调
          if (response.data.code === 0) {
            this.tableData = [];
            this.tableData = response.data.data;
          } else {
            this.$message.error(response.data.message);
          }
          this.loading = false;
        },
        function(response) {
          if (response.status === 401) {
            this.$router.push({ path: "/login" });
          }
          this.$message.error("请求失败");
          this.loading = false;
        }
      );
    },
    apPointChange(accountId, accountSite) {
      this.$prompt("请输入要变更的AP点数", "AP点数变更", {
        inputType: "number"
      })
        .then(({ value }) => {
          this.loading = true;
          this.$http
            .post(
              "/api/account/apPointChange.json?accountId=" +
                accountId +"&accountSite=" + accountSite + 
                "&point=" +
                value
            )
            .then(
              function(response) {
                // 这里是处理正确的回调
                if (response.data.code === 0) {
                  this.$alert(
                    <p>
                      操作成功！
                      <br />
                      {response.data.data}
                    </p>,
                    "提示",
                    {
                      type: "success"
                    }
                  );
                  this.billList(accountId, accountSite);
                } else {
                  this.$message.error(response.data.message);
                  this.loading = false;
                }
              },
              function(response) {
                if (response.status === 401) {
                  this.$router.push({ path: "/login" });
                }
                this.$message.error("请求失败");
                this.loading = false;
              }
            );
        })
        .catch(() => {});
    },
    removeAccount(accountId, accountSite, index) {
      let apiUrl = "/api/account/removeAccount.json?accountId=" + accountId + "&accountSite=" + accountSite;
      this.$http.get(apiUrl).then(
        function(response) {
          // 这里是处理正确的回调
          if (response.data.code === 0) {
            this.$message({
              message: "推流账户已删除",
              type: "success"
            });
            if (index !== undefined) {
              this.$refs["popover-" + index].doClose();
            } else {
              this.$refs.removePopover.doClose();
            }
            if (accountId == this.account.accountId) {
              this.account.saved = false;
              sessionStorage.setItem("account", JSON.stringify(this.account));
            }
            this.accountList();
          } else {
            this.$message.error(response.data.message);
          }
        },
        function(response) {
          if (response.status === 401) {
            this.$router.push({ path: "/login" });
          }
          this.$message.error("请求失败");
        }
      );
    },
    addAccount() {
      if (!this.agree) {
        this.$alert("同意免责声明后才能继续操作", "提示", {
          confirmButtonText: "确定",
          type: "warning"
        });
        return;
      }
      this.addVisible = false;
      this.loading = true;
      let apiUrl = "/api/account/addAccount.json";
      this.$http.post(apiUrl, this.accountInfo).then(
        function(response) {
          // 这里是处理正确的回调
          if (response.data.code === 0) {
            this.$message({
              message: "推流账户创建成功",
              type: "success"
            });
            this.account.saved = true;
            this.account.joinAutoBalance = this.accountInfo.joinAutoBalance;
            this.account.postBiliDynamic = this.accountInfo.postBiliDynamic;
            this.account.autoRoomTitle = this.accountInfo.autoRoomTitle;
            this.account.description = this.accountInfo.description;
            sessionStorage.setItem("account", JSON.stringify(this.account));
            this.accountList();
          } else {
            this.$message.error(response.data.message);
            this.loading = false;
          }
        },
        function(response) {
          if (response.status === 401) {
            this.$router.push({ path: "/login" });
          }
          this.$message.error("请求失败");
          this.loading = false;
        }
      );
    },
    billList(accountId, accountSite) {
      this.loading = true;
      this.billAccountId = accountId;
      this.billAccountSite = accountSite;
      this.billListVisible = true;
      this.$http.get("/api/account/billList.json?accountId=" + accountId + "&accountSite=" + accountSite).then(
        function(response) {
          // 这里是处理正确的回调
          if (response.data.code === 0) {
            this.billTableData = [];
            this.billTableData = response.data.data;
          } else {
            this.$message.error(response.data.message);
          }
          this.loading = false;
        },
        function(response) {
          if (response.status === 401) {
            this.$router.push({ path: "/login" });
          }
          this.$message.error("请求失败");
          this.loading = false;
        }
      );
    },
    editAccount() {
      this.editVisible = false;
      this.loading = true;
      let apiUrl = "/api/account/editAccount.json";
      this.$http.post(apiUrl, this.account).then(
        function(response) {
          // 这里是处理正确的回调
          if (response.data.code === 0) {
            this.$message({
              message: "账户设置修改成功",
              type: "success"
            });
            sessionStorage.setItem("account", JSON.stringify(this.account));
            this.accountList();
          } else {
            this.$message.error(response.data.message);
            this.loading = false;
          }
        },
        function(response) {
          if (response.status === 401) {
            this.$router.push({ path: "/login" });
          }
          this.$message.error("请求失败");
          this.loading = false;
        }
      );
    }
  },
  components: { PagedTable },
  created() {
    this.accountList();
  }
};
</script>
